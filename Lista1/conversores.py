def celsius_para_fahrenheit(a):
    return (a * (9/5)) + 32

def fahrenheit_para_celsius(a):
    return (a - 32) * (5/9)

def metro_para_pes(a):
    return a * 3.281

def pes_para_metro(a):
    return a / 3.281


def quilometro_para_milhas(a):
    return a *  1.609

def milhas_para_quilometro(a):
    return a /  1.609

def dia_para_horas(a):
    return a * 24
